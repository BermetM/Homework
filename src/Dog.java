public class Dog {
    private String name;
    private int age;
    private int weight;


    public void setName(String name1) {
        name = name1;
    }

    public String getName() {
        return name;
    }
    public void setAge(int ag){
        age=ag;
    }
    public int getAge(){
        return age;
    }
    public void setWeight(int weight1){
        weight=weight1;
    }
    public int getWeight(){
        return weight;}
}